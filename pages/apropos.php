<p class="titre">[ MongoTuto, c'est quoi ? ]</p>

<p><b>MongoTuto.com est la première référence web en Français 100% dédiée à MongoDB</b>. Toutes les informations sont tirées depuis <b>la documentation officielle</b>
de MongoDB que vous pouvez <b>facilement consulter en Anglais</b> sur le site officiel : <a href="http://docs.mongodb.org/manual/" target="_blank">"http://docs.mongodb.org/manual/"</a>.</p>

<p>Ce site web est <b>100% gratuit (et le restera)</b>, car le site se veut <b>simple</b>, <b>gratuit</b> et <b>contributeur d'une technologie open-source</b>. Le partage de l'information est donc
<b>essentiel</b>. Les lecteurs attendus peuvent venir de <b>tous horizons</b> et de <b>tous niveaux</b>, si vous êtes avancé ou expert et que certains points vous semblent vraiment
trop détaillés, voir évidents, gardez à l'esprit que <b>chaque détail peut-être utile à d'autres</b>.</p>

<div class="spacer"></div>

<p class="titre">[ Motivations de l'auteur ]</p>
<p>Ce qui m'a poussé à créer <b>MongoTuto.com</b> est le fait de voir <b>la progression et l'évolution en environement professionnel</b>, mais aussi, <b>le manque de ressources
en Français</b> telles que <b>des e-books</b> ou même <b>des sites web</b>. On peut trouver, par l'intermédiaire de moteurs de recherche, des articles traitant de MongoDB mais ceux-ci
<b>restent en nombre très limités</b> et ne présentent pas tout en détails comme <b>MongoTuto.com</b> ( ou presque :) ).</p>

<div class="spacer"></div>

<p class="titre">[ Auteur du site ]</p>
<p>Je me présente, <b>David</b>, <b>passionné d'Informatique et jeune diplômé de Master</b>. Je travaille à temps plein depuis plus d'un an au <b>Royaume-Uni</b> en tant que
